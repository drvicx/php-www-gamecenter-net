					<p>Впервые игра Prey была анонсирована в 1995 году и разрабатывалась одновременно с Duke Nukem Forever.
					   В разработке принимали участие Том Холл и Джон Ромеро, работавшие в id Software.
					   Игра использовала две <span style="color:#ff4500;">революционных</span> даже на текущий момент технологии.</p>
					 
					<p>Первая представляет собой концепцию порталов - пространственных дыр в игровом пространстве
					   с помощью которых можно в реальном времени перемещаться по большой локации без экрана подгрузки.
					   Порталы органично вписывались в игровой процесс не нарушая его.
					   Эта идея была наглым образом украдена и применена в серии совершенно пустых по сюжету игр "Portal" и "Portal 2".</p>
						
					<p>Вторая революционная идея - это возможность ходить по стенам и потолкам, в буквальном смысле переворчивая с ног на голову
					   представление о стандартном игровом процессе. В огромной локации перемещение по стенам полностью меняет восприятие
					   того места в котором вы находитесь. Мозг отказывается верить что это одно и тоже место,
					   т.к в реальной жизни человек не ходит по стенам и потолкам. И эта игровая механика также органично вписывалась в игровой процесс.
					   Тем не менее, несмотря на революционность, она до сих пор нигде не применяется.</p>
					   
					<p>19 мая 2006 3D Realms анонсировала демо-версию Prey, официальная финальная версия которой вышла 11 июля 2006 года.
					   К сожалению, студия "Human Heads" которая занималась разработкой закрылась из-за отсутствия финансирования и об игре быстро забыли.
					   На разработку игры ушло 11 лет и возможно это послужило причиной банкротства компании.
					   Сейчас, в 2021 году, стало очевидным, что проекты с таким долгим периодом разработки в итоге обречены на провал.
					   Печально известен пример игры "Сyberpunk 2077" анонс которой состоялся в 2012 году, а первая версия вышла только через 8 лет,
					   существенно отставая по дизайну и графике от современных игр.
					</p>
					
