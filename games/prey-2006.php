					   засасывая их с помощью луча телопортации на свой корабль.
					   Внутри корабля они становятся пленниками инопланетной формы жизни проводящей эксперименты над разумными живыми организмами.
					   Томми, деда Эниси и Джен разделяют. Томми удается вырваться из оков и он начинает поиски своих близких.
